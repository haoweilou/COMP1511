#include<stdio.h>

int is_vowel(int character){
    if(character=='a'||character=='e'||character=='i'||character=='o'||character=='u'){
        return 0;
    }else{
        return character;
    }
}

int main(void){
    int character=getchar();
    while(character!=EOF){
        int newch=is_vowel(character);
        if(newch!=0){
            putchar(newch);  
        }
        
        character=getchar();
    }
    
    return 0;
}
